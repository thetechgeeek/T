#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const sourcePath = path.join(root, 'docs', 'UI_Library_Checklist.md');
const outputPath = path.join(root, 'src', 'design-system', 'generated', 'uiLibraryCatalog.ts');

/**
 * @typedef {'Common' | 'Web' | 'Mobile (React Native)'} ChecklistPlatform
 * @typedef {{
 *   id: string;
 *   title: string;
 *   completed: boolean;
 *   platform: ChecklistPlatform;
 *   section: string;
 *   subsection: string;
 *   depth: number;
 *   parentId: string | null;
 * }} ChecklistItem
 */

const source = fs.readFileSync(sourcePath, 'utf8');
const lines = source.split(/\r?\n/);

/** @type {ChecklistItem[]} */
const items = [];

/** @type {string[]} */
let parentStack = [];
let section = '';
let subsection = '';
/** @type {ChecklistPlatform | ''} */
let platform = '';

/** @type {Map<string, number>} */
const slugCounts = new Map();

function slugify(value) {
	return value
		.toLowerCase()
		.replace(/[^a-z0-9]+/g, '-')
		.replace(/^-+|-+$/g, '')
		.slice(0, 120);
}

function createId(parts) {
	const base = slugify(parts.filter(Boolean).join('-'));
	const nextCount = (slugCounts.get(base) ?? 0) + 1;
	slugCounts.set(base, nextCount);
	return nextCount === 1 ? base : `${base}-${nextCount}`;
}

for (const line of lines) {
	const trimmed = line.trim();

	if (trimmed.startsWith('## ')) {
		section = trimmed.slice(3);
		subsection = '';
		platform = '';
		parentStack = [];
		continue;
	}

	if (trimmed.startsWith('### ')) {
		subsection = trimmed.slice(4);
		platform = '';
		parentStack = [];
		continue;
	}

	if (trimmed.startsWith('#### ')) {
		const candidate = trimmed.slice(5);
		if (
			candidate === 'Common' ||
			candidate === 'Web' ||
			candidate === 'Mobile (React Native)'
		) {
			platform = candidate;
			parentStack = [];
		}
		continue;
	}

	if (!platform) {
		continue;
	}

	const itemMatch = line.match(/^(\s*)- \[( |x|X)\] (.+)$/);
	if (!itemMatch) {
		continue;
	}

	const indent = itemMatch[1].length;
	const depth = Math.floor(indent / 2);
	const completed = itemMatch[2].toLowerCase() === 'x';
	const title = itemMatch[3].trim();

	while (parentStack.length > depth) {
		parentStack.pop();
	}

	const parentId = depth > 0 ? (parentStack[depth - 1] ?? null) : null;
	const id = createId([section, subsection, platform, ...parentStack, title]);

	items.push({
		id,
		title,
		completed,
		platform,
		section,
		subsection,
		depth,
		parentId,
	});

	parentStack[depth] = id;
	parentStack.length = depth + 1;
}

const platformStats = {
	common: items.filter((item) => item.platform === 'Common').length,
	web: items.filter((item) => item.platform === 'Web').length,
	mobile: items.filter((item) => item.platform === 'Mobile (React Native)').length,
};

const sectionOrder = [...new Set(items.map((item) => item.section))];
const sectionStats = sectionOrder.map((name) => ({
	name,
	count: items.filter((item) => item.section === name).length,
	completedCount: items.filter((item) => item.section === name && item.completed).length,
	openCount: items.filter((item) => item.section === name && !item.completed).length,
	commonMobileCount: items.filter(
		(item) =>
			item.section === name &&
			(item.platform === 'Common' || item.platform === 'Mobile (React Native)'),
	).length,
	commonMobileCompletedCount: items.filter(
		(item) =>
			item.section === name &&
			item.completed &&
			(item.platform === 'Common' || item.platform === 'Mobile (React Native)'),
	).length,
	commonMobileOpenCount: items.filter(
		(item) =>
			item.section === name &&
			!item.completed &&
			(item.platform === 'Common' || item.platform === 'Mobile (React Native)'),
	).length,
}));

const fileContents = `/* eslint-disable */
// Auto-generated by scripts/generate-ui-library-catalog.mjs
// Do not edit by hand.

export type ChecklistPlatform = 'Common' | 'Web' | 'Mobile (React Native)';

export interface UiLibraryChecklistItem {
\tid: string;
\ttitle: string;
\tcompleted: boolean;
\tplatform: ChecklistPlatform;
\tsection: string;
\tsubsection: string;
\tdepth: number;
\tparentId: string | null;
}

export interface UiLibraryCatalogStats {
\ttotal: number;
\tcompleted: number;
\topen: number;
\tcommon: number;
\tweb: number;
\tmobile: number;
\tcommonMobile: number;
\tsections: Array<{
\t\tname: string;
\t\tcount: number;
\t\tcompletedCount: number;
\t\topenCount: number;
\t\tcommonMobileCount: number;
\t\tcommonMobileCompletedCount: number;
\t\tcommonMobileOpenCount: number;
\t}>;
}

export const UI_LIBRARY_ITEMS: UiLibraryChecklistItem[] = ${JSON.stringify(items, null, 2)} as UiLibraryChecklistItem[];

export const UI_LIBRARY_STATS: UiLibraryCatalogStats = ${JSON.stringify(
	{
		total: items.length,
		completed: items.filter((item) => item.completed).length,
		open: items.filter((item) => !item.completed).length,
		common: platformStats.common,
		web: platformStats.web,
		mobile: platformStats.mobile,
		commonMobile: platformStats.common + platformStats.mobile,
		sections: sectionStats,
	},
	null,
	2,
)} as UiLibraryCatalogStats;
`;

fs.writeFileSync(outputPath, fileContents);

console.log(
	JSON.stringify(
		{
			outputPath: path.relative(root, outputPath),
			total: items.length,
			common: platformStats.common,
			web: platformStats.web,
			mobile: platformStats.mobile,
			commonMobile: platformStats.common + platformStats.mobile,
		},
		null,
		2,
	),
);
