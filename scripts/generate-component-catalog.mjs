#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const componentsRoot = path.join(root, 'src', 'components');
const outputPath = path.join(root, 'src', 'design-system', 'generated', 'componentCatalog.ts');

/**
 * @typedef {'atoms' | 'molecules' | 'organisms' | 'skeletons'} DesignSystemComponentKind
 * @typedef {{
 *   id: string;
 *   name: string;
 *   kind: DesignSystemComponentKind;
 *   filePath: string;
 *   hasTests: boolean;
 * }} DesignSystemComponent
 */

/**
 * @param {string} dirPath
 * @returns {string[]}
 */
function walkFiles(dirPath) {
	return fs.readdirSync(dirPath, { withFileTypes: true }).flatMap((entry) => {
		const entryPath = path.join(dirPath, entry.name);
		if (entry.name === '__tests__') {
			return [];
		}
		if (entry.isDirectory()) {
			return walkFiles(entryPath);
		}
		if (!entry.name.endsWith('.ts') && !entry.name.endsWith('.tsx')) {
			return [];
		}
		return [entryPath];
	});
}

/**
 * @param {string} value
 * @returns {string}
 */
function slugify(value) {
	return value
		.toLowerCase()
		.replace(/[^a-z0-9]+/g, '-')
		.replace(/^-+|-+$/g, '');
}

/**
 * @param {string} filePath
 * @returns {DesignSystemComponentKind}
 */
function getKind(filePath) {
	const relativePath = path.relative(componentsRoot, filePath).split(path.sep);
	if (relativePath[0] === 'molecules' && relativePath[1] === 'skeletons') {
		return 'skeletons';
	}
	if (relativePath[0] === 'atoms' || relativePath[0] === 'molecules' || relativePath[0] === 'organisms') {
		return relativePath[0];
	}
	throw new Error(`Unsupported component kind for ${filePath}`);
}

/**
 * @param {string} name
 * @param {string} relativePath
 * @returns {boolean}
 */
function hasTestFile(name, relativePath) {
	const segments = relativePath.split('/');
	const rootDir = segments[2];
	const candidates = [
		path.join(root, 'src', 'components', rootDir, '__tests__', `${name}.test.tsx`),
		path.join(root, 'src', 'components', rootDir, '__tests__', `${name}.test.ts`),
	];
	return candidates.some((candidate) => fs.existsSync(candidate));
}

/** @type {DesignSystemComponent[]} */
const components = walkFiles(componentsRoot)
	.filter((filePath) => !filePath.includes(`${path.sep}__tests__${path.sep}`))
	.map((filePath) => {
		const relativePath = path.relative(root, filePath).replaceAll(path.sep, '/');
		const name = path.basename(filePath, path.extname(filePath));
		return {
			id: slugify(relativePath),
			name,
			kind: getKind(filePath),
			filePath: relativePath,
			hasTests: hasTestFile(name, relativePath),
		};
	})
	.sort((left, right) => {
		if (left.kind !== right.kind) {
			return left.kind.localeCompare(right.kind);
		}
		return left.name.localeCompare(right.name);
	});

const stats = {
	total: components.length,
	tested: components.filter((component) => component.hasTests).length,
	byKind: {
		atoms: components.filter((component) => component.kind === 'atoms').length,
		molecules: components.filter((component) => component.kind === 'molecules').length,
		organisms: components.filter((component) => component.kind === 'organisms').length,
		skeletons: components.filter((component) => component.kind === 'skeletons').length,
	},
};

const fileContents = `/* eslint-disable */
// Auto-generated by scripts/generate-component-catalog.mjs
// Do not edit by hand.

export type DesignSystemComponentKind = 'atoms' | 'molecules' | 'organisms' | 'skeletons';

export interface DesignSystemComponent {
\tid: string;
\tname: string;
\tkind: DesignSystemComponentKind;
\tfilePath: string;
\thasTests: boolean;
}

export interface DesignSystemComponentStats {
\ttotal: number;
\ttested: number;
\tbyKind: {
\t\tatoms: number;
\t\tmolecules: number;
\t\torganisms: number;
\t\tskeletons: number;
\t};
}

export const DESIGN_SYSTEM_COMPONENTS: DesignSystemComponent[] = ${JSON.stringify(
	components,
	null,
	2,
)} as DesignSystemComponent[];

export const DESIGN_SYSTEM_COMPONENT_STATS: DesignSystemComponentStats = ${JSON.stringify(
	stats,
	null,
	2,
)} as DesignSystemComponentStats;
`;

fs.writeFileSync(outputPath, fileContents);

console.log(
	JSON.stringify(
		{
			outputPath: path.relative(root, outputPath),
			...stats,
		},
		null,
		2,
	),
);
