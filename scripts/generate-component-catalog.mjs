#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const registryPath = path.join(root, 'src', 'design-system', 'componentRegistry.json');
const outputPath = path.join(root, 'src', 'design-system', 'generated', 'componentCatalog.ts');

/**
 * @typedef {'atoms' | 'molecules' | 'organisms' | 'skeletons'} DesignSystemComponentKind
 * @typedef {{
 *   kind: DesignSystemComponentKind;
 *   filePath: string;
 * }} RegistryEntry
 * @typedef {{
 *   id: string;
 *   name: string;
 *   kind: DesignSystemComponentKind;
 *   filePath: string;
 *   hasTests: boolean;
 * }} DesignSystemComponent
 */

/**
 * @param {string} value
 * @returns {string}
 */
function slugify(value) {
	return value
		.toLowerCase()
		.replace(/[^a-z0-9]+/g, '-')
		.replace(/^-+|-+$/g, '');
}

/**
 * @param {RegistryEntry} entry
 * @returns {boolean}
 */
function hasTestFile(entry) {
	const absoluteFilePath = path.join(root, entry.filePath);
	const componentDir = path.dirname(absoluteFilePath);
	const name = path.basename(absoluteFilePath, path.extname(absoluteFilePath));
	const candidates = [
		path.join(componentDir, '__tests__', `${name}.test.tsx`),
		path.join(componentDir, '__tests__', `${name}.test.ts`),
	];

	return candidates.some((candidate) => fs.existsSync(candidate));
}

/** @type {RegistryEntry[]} */
const registry = JSON.parse(fs.readFileSync(registryPath, 'utf8'));
const seenPaths = new Set();

for (const entry of registry) {
	if (!entry?.filePath || !entry?.kind) {
		throw new Error(`Invalid component registry entry: ${JSON.stringify(entry)}`);
	}

	if (seenPaths.has(entry.filePath)) {
		throw new Error(`Duplicate component registry path: ${entry.filePath}`);
	}
	seenPaths.add(entry.filePath);

	const absoluteFilePath = path.join(root, entry.filePath);
	if (!fs.existsSync(absoluteFilePath)) {
		throw new Error(`Component registry path does not exist: ${entry.filePath}`);
	}
}

/** @type {DesignSystemComponent[]} */
const components = registry
	.map((entry) => {
		const name = path.basename(entry.filePath, path.extname(entry.filePath));
		return {
			id: slugify(entry.filePath),
			name,
			kind: entry.kind,
			filePath: entry.filePath,
			hasTests: hasTestFile(entry),
		};
	})
	.sort((left, right) => {
		if (left.kind !== right.kind) {
			return left.kind.localeCompare(right.kind);
		}
		return left.name.localeCompare(right.name);
	});

const stats = {
	total: components.length,
	tested: components.filter((component) => component.hasTests).length,
	byKind: {
		atoms: components.filter((component) => component.kind === 'atoms').length,
		molecules: components.filter((component) => component.kind === 'molecules').length,
		organisms: components.filter((component) => component.kind === 'organisms').length,
		skeletons: components.filter((component) => component.kind === 'skeletons').length,
	},
};

const fileContents = `/* eslint-disable */
// Auto-generated by scripts/generate-component-catalog.mjs
// Do not edit by hand.

export type DesignSystemComponentKind = 'atoms' | 'molecules' | 'organisms' | 'skeletons';

export interface DesignSystemComponent {
\tid: string;
\tname: string;
\tkind: DesignSystemComponentKind;
\tfilePath: string;
\thasTests: boolean;
}

export interface DesignSystemComponentStats {
\ttotal: number;
\ttested: number;
\tbyKind: {
\t\tatoms: number;
\t\tmolecules: number;
\t\torganisms: number;
\t\tskeletons: number;
\t};
}

export const DESIGN_SYSTEM_COMPONENTS: DesignSystemComponent[] = ${JSON.stringify(
	components,
	null,
	2,
)} as DesignSystemComponent[];

export const DESIGN_SYSTEM_COMPONENT_STATS: DesignSystemComponentStats = ${JSON.stringify(
	stats,
	null,
	2,
)} as DesignSystemComponentStats;
`;

fs.writeFileSync(outputPath, fileContents);

console.log(
	JSON.stringify(
		{
			outputPath: path.relative(root, outputPath),
			registryPath: path.relative(root, registryPath),
			...stats,
		},
		null,
		2,
	),
);
