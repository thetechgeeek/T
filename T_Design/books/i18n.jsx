const LocaleCtx = React.createContext(null);

function LocaleProvider({ lang, currency, children }) {
  const loc = LOCALES[lang] || LOCALES.en;
  const cur = CURRENCIES[currency] || CURRENCIES.INR;

  const t = React.useCallback((key) => {
    const dict = STRINGS[lang] || STRINGS.en;
    return dict[key] ?? STRINGS.en[key] ?? key;
  }, [lang]);

  const money = React.useCallback((n, code) => {
    const c = CURRENCIES[code] || cur;
    try {
      return new Intl.NumberFormat(loc.locale, {
        style: 'currency', currency: c.code,
        maximumFractionDigits: c.decimals, minimumFractionDigits: 0,
      }).format(n);
    } catch {
      return c.symbol + n.toLocaleString();
    }
  }, [loc.locale, cur]);

  const number = React.useCallback((n) => {
    try { return new Intl.NumberFormat(loc.locale).format(n); }
    catch { return String(n); }
  }, [loc.locale]);

  const date = React.useCallback((d) => {
    const dt = d instanceof Date ? d : new Date(d);
    try {
      return new Intl.DateTimeFormat(loc.locale, { day:'numeric', month:'short', year:'numeric' }).format(dt);
    } catch { return dt.toDateString(); }
  }, [loc.locale]);

  const value = { lang, currency, dir: loc.dir, t, money, number, date, localeName: loc.name, currencySym: cur.symbol };
  return <LocaleCtx.Provider value={value}>{children}</LocaleCtx.Provider>;
}

function useLocale() { return React.useContext(LocaleCtx); }

window.I18n = { LOCALES, CURRENCIES, STRINGS };
window.LocaleProvider = LocaleProvider;
window.useLocale = useLocale;
